"""Generate ``phrase_bank/SUMMARY.md`` from the tagged phrase bank.

This is the human-readable artefact the user asked for: "summary of the
database downloaded and the scenarios built". Counts per bucket, counts per
source, and three sample phrasings per bucket so a reviewer can sanity-check
the heuristics in ``tagging.py`` at a glance.
"""

from __future__ import annotations

import json
import logging
from collections import Counter, defaultdict
from datetime import date
from pathlib import Path

from .paths import (
    PHRASE_BANK_JSON,
    PHRASE_BANK_SUMMARY_MD,
    UNCLASSIFIED_JSON,
    ensure_dirs,
)
from .tagging import BUCKET_ORDER

log = logging.getLogger(__name__)


_LICENSES = {
    "sgd": "CC BY-SA 4.0 — Google Research, Schema-Guided Dialogue (DSTC8)",
    "multiwoz": "MIT — pfb30/multi_woz_v22, MultiWOZ 2.2",
}


def _load_bank() -> list[dict]:
    if not PHRASE_BANK_JSON.exists():
        return []
    return json.loads(PHRASE_BANK_JSON.read_text(encoding="utf-8"))


def _load_unclassified() -> list[dict]:
    if not UNCLASSIFIED_JSON.exists():
        return []
    return json.loads(UNCLASSIFIED_JSON.read_text(encoding="utf-8"))


def _format_samples(by_bucket: dict[str, list[dict]], k: int = 3) -> str:
    out: list[str] = []
    for bucket in BUCKET_ORDER:
        entries = by_bucket.get(bucket, [])
        if not entries:
            out.append(f"### `{bucket}` — _no entries_\n")
            continue
        out.append(f"### `{bucket}` — {len(entries)} entries\n")
        for entry in entries[:k]:
            text = entry.get("expression_text", "").replace("\n", " ")
            src = entry.get("source_dataset", "?")
            svc = entry.get("source_service", "")
            ctx = entry.get("original_context", "").replace("\n", " ")
            out.append(f"- _{text}_ (`{src}/{svc}`)")
            if ctx and ctx != entry.get("expression_text"):
                out.append(f"    - original: {ctx}")
        out.append("")
    return "\n".join(out)


def summarize() -> Path:
    """Compose and write ``SUMMARY.md``. Returns the output path."""
    ensure_dirs()

    bank = _load_bank()
    unclassified = _load_unclassified()

    total = len(bank)
    by_bucket: dict[str, list[dict]] = defaultdict(list)
    by_source: Counter[str] = Counter()
    by_service: Counter[str] = Counter()
    for entry in bank:
        by_bucket[entry.get("difficulty_tag", "?")].append(entry)
        by_source[entry.get("source_dataset", "?")] += 1
        by_service[
            f"{entry.get('source_dataset', '?')}/{entry.get('source_service', '?')}"
        ] += 1

    bucket_table = "\n".join(
        f"| `{bucket}` | {len(by_bucket.get(bucket, []))} |"
        for bucket in BUCKET_ORDER
    )

    source_table = "\n".join(
        f"| `{src}` | {count} | {_LICENSES.get(src, '—')} |"
        for src, count in by_source.most_common()
    )

    service_table = "\n".join(
        f"| `{svc}` | {count} |" for svc, count in by_service.most_common()
    )

    md = f"""# Phrase Bank — SUMMARY

_Auto-generated by `python -m evaluation.mining.cli summarize` on {date.today().isoformat()}._

This file is the canonical at-a-glance view of the phrase bank that feeds the
evaluation simulator's `frozen_phrasing` slot (EVALUATION_FRAMEWORK.md §2a, §5).
Re-run the mining + tagging + summarize CLI to refresh.

## Totals

| Metric | Value |
|--------|-------|
| Total tagged expressions | **{total}** |
| Unclassified (manual review needed) | {len(unclassified)} |
| Distinct buckets observed | {sum(1 for b in BUCKET_ORDER if by_bucket.get(b))} |

## By difficulty bucket

| Bucket | Count |
|--------|-------|
{bucket_table}

> **Reading guide.** The five seed buckets (specific date, relative, vague,
> compound, unsupported) are the framework's pre-allocated set. The discovery
> buckets (deadline-driven, negative constraint, open-ended) appear only when
> the heuristics detect ≥3 phrasings (Section 5). See `bucket_log.md` for any
> additions beyond the seeds.

## By source dataset

| Source | Count | License |
|--------|-------|---------|
{source_table}

## By source service (top-level taxonomy traceability)

| Source / service | Count |
|------------------|-------|
{service_table}

## Sample phrasings per bucket

{_format_samples(by_bucket)}

## Unclassified bucket (top 10 for manual review)

These are entries the heuristics in `tagging.py` could not bucket. They are
written to `_unclassified.json`. The intended workflow is: review, decide
whether (a) the heuristics should be widened, (b) a new discovery bucket is
warranted (log it in `bucket_log.md`), or (c) the entry should be dropped.

"""

    if unclassified:
        md += "\n".join(
            f"- _{e.get('expression_text', '')}_  — _{e.get('source_dataset', '?')}/{e.get('source_service', '?')}_"
            for e in unclassified[:10]
        )
    else:
        md += "_All mined expressions were classified._"

    md += "\n"
    PHRASE_BANK_SUMMARY_MD.write_text(md, encoding="utf-8")
    log.info("Wrote summary: %s", PHRASE_BANK_SUMMARY_MD)
    return PHRASE_BANK_SUMMARY_MD
